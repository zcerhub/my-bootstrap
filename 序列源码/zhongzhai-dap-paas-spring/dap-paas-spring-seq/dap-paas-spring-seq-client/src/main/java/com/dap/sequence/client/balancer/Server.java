package com.dap.sequence.client.balancer;

public class Server {
    private java.lang.String host;
    private int port;

    public Server(java.lang.String host, int port) {
        this.host = host;
        this.port = port;
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }
}
